var (
	rectangle = image.Rectangle{
		Min: image.Point{0, 0},
		Max: image.Point{64, 64},
	}
	key = image.NewRGBA(rectangle)
)

func init() {
	for y, line := range data {
		for x, pix := range line {
			switch pix {
			case 0:
				key.SetRGBA(x, y, color.RGBA{0, 0, 0, 0})
			case 1:
				key.SetRGBA(x, y, color.RGBA{0, 0, 0, 255})
			default:
				key.SetRGBA(x, y, color.RGBA{255, 255, 255, 255})
			}
		}
	}
}

func Key() *image.RGBA {
	return key
}

